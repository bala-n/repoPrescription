package com.gt.prescriptor.utils;

public class PrescriptionRecord {
	
private String tabName;
private float mQty;
private float aQty;
private float nQty;

public float getmQty() {
	return mQty;
}
public void setmQty(float mQty) {
	this.mQty = mQty;
}
public float getaQty() {
	return aQty;
}
public void setaQty(float aQty) {
	this.aQty = aQty;
}
public float getnQty() {
	return nQty;
}
public void setnQty(float nQty) {
	this.nQty = nQty;
}
public String getTabName() {
	return tabName;
}
public void setTabName(String tabName) {
	this.tabName = tabName;
}


}
